    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
          try{
              
            String mat1 = txtMat.getText();
            String sur1 = txtSur.getText();
            String dept1= txtDept.getText();
            String gpa1 = txtGpa.getText();
            
            int mat2 = Integer.parseInt(mat1);
            float gpa2 = Float.parseFloat(gpa1);
            
            //TO UPDATE DATA TO THE DATABASE
            rs.updateInt("Mat", mat2);
            rs.updateString("SURNAME", sur1);
            rs.updateString("DEPT", dept1);
            rs.updateFloat("GPA", gpa2);
            
            rs.updateRow();
            
            JOptionPane.showMessageDialog(Klasa.this, "Record updated");
              
          }catch(SQLException err){
              
              JOptionPane.showMessageDialog(Klasa.this, err.getMessage());
          }
    }//GEN-LAST:event_btnUpdateActionPerformed

