    public void Konet(){
        
        try{
            
        String host = "jdbc:derby://localhost:1527/STUD";
        String uName = "Daniel";
        String uPass = "12345";
        
        
        // WE WILL THEN USE CONNECTION TO CONNECT AND PICK THE CONNECTION THAT WILL CONTAIN THREE VARIABLE AS SPECIFIED IN THE 
        con = DriverManager.getConnection(host, uName, uPass);
        
        // THIS CREATE STATEMENT ALLOWS US TO SLECT MODE
        stm = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
        //ERITE ANY SQL STATEMENT
        String sql = "SELECT * FROM STUD";
        rs = stm.executeQuery(sql);
        rs.next();
        
        int mat1 = rs.getInt("MAT");
        String sur = rs.getString("SURNAME");
        String dept1 = rs.getString("DEPT");
        float gpa = rs.getFloat("GPA");
        
        //NOW WE WANT TO TRANSFER THE VALUES TO OUR FORM BUT THE INTEGERS AND NON STRINGS TYPE MUST FIRST BE CONVERTED TO STRINGS
        String mat2 = Integer.toString(mat1);
        String gpa2 = Float.toString(gpa);
        
        // NOW WE CAN ADD THEM TO THE TEXT BOXES CONSIDERING THE VARIABLE NAMES
        txtMat.setText(mat2);
        txtSur.setText(sur);
        txtDept.setText(dept1);
        txtGpa.setText(gpa2);
            
        }catch(SQLException err){
            JOptionPane.showMessageDialog(Klasa.this, err.getMessage());
        }
        
    }
